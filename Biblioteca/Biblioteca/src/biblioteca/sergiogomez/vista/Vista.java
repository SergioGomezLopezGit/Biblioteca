    @Override
    public void actionPerformed(ActionEvent e) {
        switch(e.getActionCommand()){
            case "Login":
                abrirDialogoLogin();
                break;
            case "CheckUsuario":
                comprobarUsuario();
                break;
                
        }
    }

    private void anadirListeners() {
        abrirDialogo.addActionListener(this);
        checkUsuario.addActionListener(this);
    }

    private void abrirDialogoLogin() {
        LoginUsuario.setLocationRelativeTo(this);
        LoginUsuario.setTitle("Login de usuario");
        LoginUsuario.setVisible(true);
    }

    private void comprobarUsuario() {
        if(usuario.getText().isEmpty() || contrasena.getText().isEmpty()){
            aviso.setText("Debe completar los campos usuario y contrase√±a.");
        }else{
            if(conexion.comprobarUsuario(usuario.getText().toString(), contrasena.getText().toString())){
                abrirSesion();
                LoginUsuario.dispose();
            }else{
                aviso.setText("Usuario o contrase√±a no validos.");
            }
            
        }
    }

    private void abrirSesion() {
        try {
            sesion=conexion.obtenerUsuario(usuario.getText().toString());
        } catch (SQLException ex) {
            //crear un joptionpane para mostrar si ha ocurrido un error con la sesion
        }
    }
