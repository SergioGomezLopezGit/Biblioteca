    private void preActualizar(){
        int index=filaSeleccionada;
        try{
            if(actualizado==false){
                Titulo.setText(modeloTabla.getValueAt(filaSeleccionada, 0).toString());
                isbn.setText(modeloTabla.getValueAt(filaSeleccionada, 1).toString());
                add.setEnabled(false);
                borrar.setEnabled(false);
                actualizado=true;
            }else{
                add.setEnabled(true);
                borrar.setEnabled(true);
                actualizar(index);
                actualizado=false;
                vaciarCampos();
            }
        }catch(IndexOutOfBoundsException iobe){
            JOptionPane.showMessageDialog(null,"No has seleccionado ninguna fila campo.","Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void actualizar(int index){
        if(!isbn.getText().isEmpty() && !Titulo.getText().isEmpty()){
            if( Principal.getBiblioteca().getLibros().get(index).getTitulo().equalsIgnoreCase(Titulo.getText()) || !Principal.getConexion().existeUsuario(Titulo.getText())){
                Principal.getBiblioteca().getLibros().get(index).setTitulo(Titulo.getText());
                Principal.getBiblioteca().getLibros().get(index).setISBN(isbn.getText());
                Principal.getConexion().actualizarLibros(Principal.getBiblioteca().getLibros().get(index));                
                modeloTabla.fireTableDataChanged();
                vaciarCampos();
            }else{
                JOptionPane.showMessageDialog(null,"El nombre de usuario ya existe en el sistema.","Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null,"Debes introducir todos los campos.","Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void anadirLibro() {
        if(!isbn.getText().isEmpty() && !Titulo.getText().isEmpty()){
            if(!Principal.getConexion().existeLibro(isbn.getText())){
                Libro libro=new Libro(Titulo.getText(),isbn.getText(),0);
                Principal.getBiblioteca().getLibros().add(libro);
                Principal.getConexion().addLibro(libro);
                modeloTabla.fireTableDataChanged();
                vaciarCampos();
            }else{
                JOptionPane.showMessageDialog(null,"El ISBN de libro ya existe en el sistema.","Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null,"Debes rellenar todos los campos.","Error", JOptionPane.ERROR_MESSAGE);
        } 
    }
    
    private void borrarLibro(){
        try{
            if(filaSeleccionada!=-1){
                Principal.getConexion().borrarLibro(Principal.getBiblioteca().getLibros().get(filaSeleccionada).getISBN());
                Principal.getBiblioteca().getLibros().remove(filaSeleccionada);
                modeloTabla.fireTableDataChanged();
            }else{
                JOptionPane.showMessageDialog(null,"Selecciona una fila.","Error", JOptionPane.ERROR_MESSAGE);
            }
        }catch(IndexOutOfBoundsException iobe){
            JOptionPane.showMessageDialog(null,"Selecciona una fila.","Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void vaciarCampos(){
        Titulo.setText("");
        isbn.setText("");
    }

    private void iniciarListeners() {
        jTable1.addMouseListener(this);
        add.addActionListener(this);
        borrar.addActionListener(this);
        modificar.addActionListener(this);
    }
