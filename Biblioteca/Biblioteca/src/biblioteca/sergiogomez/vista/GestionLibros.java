    @Override
    public void actionPerformed(ActionEvent e) {
        switch(e.getActionCommand()){
            case "Borrar":
                borrarLibro();
                break;
            case "Modificar":
                preActualizar();
                break;
            case "Añadir":
                anadirLibro();
                break;        
        }
    }

